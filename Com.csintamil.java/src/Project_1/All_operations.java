package Project_1;

public class All_operations {

	public void Add()
		{
		int a=50;
		int b=1;
		int c=a+b;
		System.out.println("a: "+a);
		System.out.println("b: "+b);
		System.out.println("Addition of a and b: "+c);
		}
	public void Sub()
		{
		int a=100;
		int b=20;
		int c=a-b;
		System.out.println("a: "+a);
		System.out.println("b: "+b);
		System.out.println("Subtraction of a and b: "+c);
		}
	public void Mul()
		{
		int a=5;
		int b=10;
		int c=a*b;
		System.out.println("a: "+a);
		System.out.println("b: "+b);
		System.out.println("Result of Multiplication: "+c);
		}
	
}
